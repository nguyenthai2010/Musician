                            	In 2007, he moved from Belgium to Los Angeles, a great opportunity to further develop his passion for film music. He wrote a variety of soundtracks and enrolled in the UCLA Certificate Program in Film Scoring, considered one of the top film scoring programs in the world. Since then, he has written soundtracks for several exciting projects. In 2009, he got hired as a music production assistant by Marc Streitenfeld, who at that time was composing the music for Ridley Scott’s feature film Robin Hood. After that, he was brought on as a Midi Orchestrator for 2 projects by Heitor Perreira at Hans Zimmer’s studio, Remote Control.
