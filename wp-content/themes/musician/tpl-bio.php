                           	Sven Faulconer was born in Belgium and started studying music at the age of 6. He quickly developed a taste for improvising on the piano and soon began writing his own pieces. Throughout the years, Sven became a passionate clarinet player and performed a number of clarinet solo concertos with symphonic orchestra.
                           </p>
